  static List<Product> getFlashSaleProducts() {
    return products.where((product) => product.isFlashSale).toList();
  }

  static List<Product> getProductsByCategory(String categoryId) {
    return products.where((product) => product.categoryId == categoryId).toList();
  }

  static Product? getProductById(String productId) {
    return products.cast<Product?>().firstWhere(
          (product) => product?.id == productId,
          orElse: () => null,
        );
  }

  static Category? getCategoryById(String categoryId) {
    return categories.cast<Category?>().firstWhere(
          (category) => category?.id == categoryId,
          orElse: () => null,
        );
  }

  static List<Product> getTrendingProducts() {
    return products.where((product) => product.rating >= 4.5).toList();
  }

  static List<Product> searchProducts(String query) {
    final lowercaseQuery = query.toLowerCase();
    return products.where((product) =>
        product.name.toLowerCase().contains(lowercaseQuery) ||
        product.description.toLowerCase().contains(lowercaseQuery) ||
        product.brand.toLowerCase().contains(lowercaseQuery) ||
        product.categoryName.toLowerCase().contains(lowercaseQuery)).toList();
  }
